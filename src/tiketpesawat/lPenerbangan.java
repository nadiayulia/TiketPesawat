    private void btUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btUpdateActionPerformed
        // TODO add your handling code here:
        try {
            jadwalpenerbangan p = new jadwalpenerbangan();
            this.stat = k.getCon().prepareStatement("update jadwalpenerbangan set nama_maskapai=?,"
                    + "tujuan_penerbangan=?,tanggal_penerbangan=?,jam_penerbangan=?,jenis_penerbangan=?,harga=?,status_ketersediaan=? where id_penerbangan=?");
            stat.setString(1, p.nama_maskapai);
            stat.setString(2, p.tujuan_penerbangan);
            stat.setString(3, p.tanggal_penerbangan);
            stat.setString(4, p.jam_penerbangan);
            stat.setString(5, p.jenis_penerbangan);
            stat.setString(6, p.harga);
            stat.setString(7, p.status_ketersediaan);
            stat.setString(8, IDPenerbanganText.getText());
            stat.executeUpdate();
            refreshTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btUpdateActionPerformed

