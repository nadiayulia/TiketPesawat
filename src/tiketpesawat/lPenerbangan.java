    public void refreshTable(){
        model = new DefaultTableModel();
        model.addColumn("ID Masakan");
        model.addColumn("Nama Maskapai");
        model.addColumn("Tujuan Penerbangan");
        model.addColumn("Tanggal Penerbangan");
        model.addColumn("Jam Penerbangan");
        model.addColumn("Jenis Penerbangan");
        model.addColumn("Harga");
        model.addColumn("Status Ketersediaan");
        tableJadwal.setModel(model);
        
        try {
            this.stat = k.getCon().prepareStatement("select * from jadwalpenerbangan");
            this.rs = this.stat.executeQuery();
            while (rs.next()) {                
               Object[] data = { 
                   rs.getInt("id_penerbangan"),
                   rs.getString("nama_maskapai"),
                   rs.getString("tujuan_penerbangan"),
                   rs.getString("tanggal_penerbangan"),
                   rs.getString("jam_penerbangan"),
                   rs.getString("jenis_penerbangan"),
                   rs.getString("harga"),
                   rs.getString("status_ketersediaan")
               };
              model.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        IDPenerbanganText.setText("");
        maskapaiText.setText("");
        tanggalText.setText("");
        hargaText.setText("");
    }
